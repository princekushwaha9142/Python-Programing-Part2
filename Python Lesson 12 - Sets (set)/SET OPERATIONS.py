a = {1, 2, 3, 4}
b = {3, 4, 5, 6}

# Union (Combine)
print(a | b)

# Output: {1,2,3,4,5,6}

# Intersection (Common)

print(a & b)

# Output: {3,4}

# Difference

print(a - b)

# Output: {1,2}

# Symmetric Difference 
print(a ^ b)

# Output: {1,2,5,6}

